/* ============================================================
 * 韓国書籍シート作成
 * ============================================================ */
function 韓国書籍シートを作成() {
  const ui = SpreadsheetApp.getUi();
  const ss = SpreadsheetApp.getActive();
  const シート名 = 設定_韓国書籍.マスターシート名;

  if (ss.getSheetByName(シート名)) {
    ui.alert(`「${シート名}」シートは既に存在します`);
    return;
  }

  const sh = ss.insertSheet(シート名);

  const ヘッダー = [
    '発番発行',
    '登録状況',
    '商品コード(SKU)',
    'タイトル',

    '作者',
    '日本語タイトル',
    '原題タイトル',
    '原題商品タイトル',
    'リンク',

    '形態(通常/初回限定/特装)',
    '言語',
    'カテゴリ',

    '単巻数',
    'セット巻数開始番号',
    'セット巻数終了番号',

    '特典メモ',
    'ISBN',

    '売価',
    '原価',
    '粗利益率',
    '配送パターン',

    '商品説明',
    'サイト商品コード',
    'メイン画像',
    '追加画像',
    '発売日',

    '作品ID(W)(自動)',
    'SKU(自動)',
    '商品コードステータス',
    '重複チェックキー',

    '登録日',
    '登録者',
    '備考'
  ];

  sh.getRange(1, 1, 1, ヘッダー.length).setValues([ヘッダー]);

  const 自動列 = [
    '商品コード(SKU)',
    'タイトル',
    '作品ID(W)(自動)',
    'SKU(自動)',
    '商品コードステータス',
    '重複チェックキー',
    '登録日'
  ];

  const API列 = [
    '原題商品タイトル',
    'リンク',
    'ISBN',
    '原価',
    'サイト商品コード',
    'メイン画像',
    '追加画像',
    '発売日'
  ];

  const 入力重要列 = [
    '作者',
    '日本語タイトル',
    '原題タイトル',
    '形態(通常/初回限定/特装)',
    '言語',
    'カテゴリ',
    '売価',
    '配送パターン'
  ];

  for (let i = 0; i < ヘッダー.length; i++) {
    const h = ヘッダー[i];
    let 色 = '#4a86e8';

    if (自動列.includes(h)) 色 = '#999999';
    if (API列.includes(h)) 色 = '#e69138';
    if (入力重要列.includes(h)) 色 = '#3c78d8';

    sh.getRange(1, i + 1)
      .setBackground(色)
      .setFontColor('#ffffff')
      .setFontWeight('bold')
      .setHorizontalAlignment('center')
      .setVerticalAlignment('middle');
  }

  sh.setRowHeight(1, 40);
  sh.setFrozenRows(1);
  sh.setFrozenColumns(3);

  const 最終行 = 1000;

  // 発番発行チェックボックス
  sh.getRange(2, 1, 最終行 - 1, 1).insertCheckboxes();

  // 登録状況プルダウン
  sh.getRange(2, 2, 最終行 - 1, 1).setDataValidation(
    SpreadsheetApp.newDataValidation()
      .requireValueInList(['未登録', '登録済み'], true)
      .build()
  );

  // 未登録行の背景色
  sh.setConditionalFormatRules([
    SpreadsheetApp.newConditionalFormatRule()
      .whenFormulaSatisfied('=$B2="未登録"')
      .setBackground('#ffff00')
      .setRanges([sh.getRange(2, 1, 最終行 - 1, ヘッダー.length)])
      .build()
  ]);

  const 幅設定 = {
    1: 80,
    2: 90,
    3: 130,
    4: 420,
    5: 180,
    6: 260,
    7: 220,
    8: 260,
    9: 220,
    10: 180,
    11: 90,
    12: 120,
    13: 90,
    14: 150,
    15: 150,
    16: 180,
    17: 150,
    18: 90,
    19: 90,
    20: 100,
    21: 130,
    22: 300,
    23: 150,
    24: 220,
    25: 260,
    26: 120,
    27: 130,
    28: 130,
    29: 170,
    30: 180,
    31: 120,
    32: 100,
    33: 200
  };

  Object.keys(幅設定).forEach(col => {
    sh.setColumnWidth(Number(col), 幅設定[col]);
  });

  sh.getRange(1, 1, 最終行, ヘッダー.length)
    .setWrapStrategy(SpreadsheetApp.WrapStrategy.CLIP)
    .setVerticalAlignment('middle');

  ui.alert(
    '✅ 韓国書籍シートを作成しました\n\n' +
    '次にメニュー「韓国書籍 → プルダウン更新」を実行してください'
  );
}