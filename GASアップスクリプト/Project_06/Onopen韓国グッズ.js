/* ============================================================
 * onOpen
 * ============================================================ */
function onOpen() {
  const ui = SpreadsheetApp.getUi();
  const menu = ui.createMenu('🛍 韓国グッズ管理');

  menu.addItem('① 商品コード確定発行', '韓国グッズ_確定発行')
      .addItem('② プルダウン更新', '韓国グッズ_プルダウン更新')
      .addItem('③ 重複チェック', '韓国グッズ_重複チェック')
      .addSeparator()
      .addSubMenu(
        ui.createMenu('シート作成・初期設定')
          .addItem('韓国グッズシートを作成', '韓国グッズシートを作成')
          .addItem('Works（韓国グッズ）シートを作成', '韓国グッズWorksシートを作成')
          .addItem('グッズ作品マスターを作成', 'グッズ作品マスターを作成')
          .addSeparator()
          .addItem('Works初期化（全削除）', '韓国グッズ_Works初期化')
      )
      .addToUi();
}

/* ============================================================
 * onEdit トリガー
 * ============================================================ */
function onEdit(e) {
  韓国グッズ_onEdit(e);
}